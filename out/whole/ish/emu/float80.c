float80 f80_round(float80 f) {
    if (!f80_is_supported(f))
        return ((float80) {.signif = 0xc000000000000000, .exp = 0x7fff, .sign = 0});

    int bits_to_clear = 63 - unbias(f.exp);
    if (bits_to_clear <= 0)
        return f;

    f = f80_shift_right(f, bits_to_clear);
    if (f.signif == 0) {

        f.exp = 0;
    } else {
        f = f80_normalize(f);
    }
    return f;
}

