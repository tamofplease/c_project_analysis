static inline unsigned int countLeadingZeros(int x)
{
 int i;
 const unsigned bits = sizeof(x) * 8;
 unsigned count = bits;

 if (x < 0) {
  return 0;
 }
 for (i = bits; --i; ) {
  if (x == 0) break;
  count--;
  x >>= 1;
 }

 return count;
}
