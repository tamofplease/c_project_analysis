static TAG_CALLBACK(spp_ifeq) {
 char *value = buf;
 char *eq = strchr(buf, ' ');
 if (eq) {
  *eq = '\0';
  value = spp_var_get(value);
  if (value && !strcmp(value, eq+1)) {
   state->echo[state->ifl + 1] = 1;
  } else state->echo[state->ifl + 1] = 0;

 } else {
  value = spp_var_get(buf);
  if (!value || *value=='\0')
   state->echo[state->ifl + 1] = 1;
  else state->echo[state->ifl + 1] = 0;

 }
 return 1;
}

static TAG_CALLBACK(spp_hex) {
 int i;
 for(i = 0; buf[i]; i++) {
  if (buf[i] >= '0' && buf[i] <= '9') {
   int b;
   unsigned int ch;
   b = buf[i + 2];
   buf[i + 2] = '\0';
   sscanf(buf + i, "%02x", &ch);
   out_printf (out, "%c", ch);
   buf[i + 2] = b;
   buf = buf + 2;
  }
 }
 return 0;
}

static TAG_CALLBACK(spp_grepline) {
 FILE *fd;
 char b[1024];
 char *ptr;
 int line;

 if (!state->echo[state->ifl]) return 1;
 ptr = strchr(buf, ' ');
 if (ptr) {
  *ptr= '\0';
  fd = fopen (buf, "r");
  line = atoi (ptr+1);
  if (fd) {
   while (!feof (fd) && line--) {
    if (!fgets (b, 1023, fd)) {
     break;
    }
   }
   fclose (fd);
   out_printf (out, "%s", b);
  } else {
   fprintf(stderr, "Unable to open '%s'\n", buf);
  }
 }
 return 0;
}

