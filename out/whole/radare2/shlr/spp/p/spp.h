static TAG_CALLBACK(spp_set) {
 char *eq, *val = "";
 if (!state->echo[state->ifl]) {
  return 0;
 }
 for (eq=buf; eq[0]; eq++) {
  switch (eq[0]) {
  case '-':
  case '.':
   eq[0] = '_';
   break;
  }
 }
 eq = strchr (buf, ' ');
 if (eq) {
  *eq = '\0';
  val = eq + 1;
 }
 if (spp_var_set (buf, val) == -1) {
  fprintf (stderr, "Invalid variable name '%s' at line %d\n", buf, state->lineno);
 }
 return 0;
}

